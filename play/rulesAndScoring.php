	<div id="rulesAndScoringBodyDiv">
	<ul>
		<span class='ruleSpan'>BATTING POINTS</span>
		<li>Base Score:1 pointper run.</li>
		<li>Impact Score:3 points per six, 1 point per four, -5 for a duck.</li>
		<li>Milestone Bonus:10 points for 50 runs, 15 more points for 75 runs, 20 more points for 100 runs, and so on.</li>
		<li>Pace Bonus:Runs scored – Balls faced. Doubled if positive, halved if negative.</li>
	</ul>
	<ul>
		<span class='ruleSpan'>BOWLING POINTS</span>
		<li>Base Score:30 points per wicket.</li>
		<li>Impact Score:15 points for a maiden over.</li>
		<li>Milestone Bonus:10 points for 2 wickets, 15 more points for 3 wickets, 20 more points for 4 wickets, and so on.</li>
		<li>Pace Bonus:Balls bowled – Runs conceded.</li>
	</ul>
	<ul>
		<span class='ruleSpan'>FIELDING POINTS</span>
		<li>Catch:15 points.</li>
		<li>Stumping:20 points.</li>
		<li>Direct Hit:20 points.</li>
		<li>Run Out:15 points for each player involved.</li>
	</ul>
	<ul>
		<span class='ruleSpan'>BONUS POINTS</span>
		<li>Man of the Match:50 points.</li>
		<li>Victory Bonus:10 points for victory in Stages 1&2.15 points for victory in Stage 3. 20 points for victory in Stage 4.</li>
	</ul>
